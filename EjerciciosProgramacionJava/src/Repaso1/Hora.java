package Repaso1;

public class Hora {

    private int hora;
    private int minuto;

    public Hora(int hora, int minuto) {
        this.hora = hora;
        this.minuto = minuto;
    }

    @Override
    public String toString() {
        return "Hora{" + "hora=" + hora + ", minuto=" + minuto + '}';
    }
    
    

}
